using System;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using Simasoft.LojaWebModerna.Dominio.Entidades;
using Flunt.Validations;

namespace Simasoft.LojaWebModerna.Dominio.Testes
{
    [TestClass]
    public class ClienteTeste
    {

        private readonly Usuario _usuario = new Usuario("gabrielsimas","abc,123");

        [TestMethod]
        [TestCategory("Cliente - Novo Cliente")]
        public void Dado_um_nome_invalido_deve_retornar_uma_notificacao()
        {            
            var cliente = new Cliente("","Simas",new DateTime(1982,06,26),"gabrielsimas@gmail.com",_usuario);
            Assert.IsFalse(cliente.Invalid);
        }

        [TestMethod]
        [TestCategory("Cliente - Novo Cliente")]
        public void Dado_um_sobrenome_invalido_deve_retornar_uma_notificacao()
        {
            var cliente = new Cliente("Gabriel","",new DateTime(1982,06,26),"gabrielsimas@gmail.com",_usuario);
            Assert.IsFalse(cliente.Invalid);            
        }

        [TestMethod]
        [TestCategory("Cliente - Novo Cliente")]
        public void Dado_um_email_invalido_deve_retornar_uma_notificacao()
        {
            var cliente = new Cliente("Gabriel","Simas",new DateTime(1982,06,26),"a",_usuario);
            Assert.IsFalse(cliente.Invalid);            
        }
    }
}
